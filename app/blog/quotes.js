const DAY_IN_MS = 24 * 60 * 60 * 1000;

function getUtcDayNumber(date = new Date()) {
  return Math.floor(
    Date.UTC(date.getUTCFullYear(), date.getUTCMonth(), date.getUTCDate()) / DAY_IN_MS
  );
}

function createSeededRandom(seed) {
  let state = seed >>> 0;

  return () => {
    state = (state + 0x6d2b79f5) | 0;
    let t = Math.imul(state ^ (state >>> 15), 1 | state);
    t ^= t + Math.imul(t ^ (t >>> 7), 61 | t);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function getShuffledOrder(length, seed) {
  const indices = Array.from({ length }, (_, i) => i);
  const random = createSeededRandom(seed);

  for (let i = indices.length - 1; i > 0; i -= 1) {
    const j = Math.floor(random() * (i + 1));
    [indices[i], indices[j]] = [indices[j], indices[i]];
  }

  return indices;
}

export function getQuoteOfTheDay(date = new Date()) {
  if (!Array.isArray(BLOG_QUOTES) || BLOG_QUOTES.length === 0) {
    return null;
  }

  const dayNumber = getUtcDayNumber(date);
  const cycleLength = BLOG_QUOTES.length;
  const cycle = Math.floor(dayNumber / cycleLength);
  const positionInCycle = dayNumber % cycleLength;
  const order = getShuffledOrder(cycleLength, cycle + 1);
  const quoteIndex = order[positionInCycle];

  return BLOG_QUOTES[quoteIndex];
}
